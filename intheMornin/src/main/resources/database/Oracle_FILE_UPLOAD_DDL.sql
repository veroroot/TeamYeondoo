DROP TABLE CUSTOMERS;
DROP TABLE MANAGERS;
DROP TABLE CLOTHES;
DROP TABLE OUTFITS;

CREATE TABLE CUSTOMERS (
        CUST_ID VARCHAR(30) PRIMARY KEY,
        CUST_PASSWORD VARCHAR(20) NOT NULL,
        CUST_NAME VARCHAR(20) NOT NULL,
        CUST_ADDRESS VARCHAR(100) NOT NULL,
        CUST_GENDER VARCHAR(20) NOT NULL,
        CUST_RANK VARCHAR(20) NULL,
        CUST_POINT INT NULL
        );

CREATE TABLE MANAGERS (
        MANA_ID VARCHAR(30) PRIMARY KEY,
        MANA_PASSWORD VARCHAR(20) NOT NULL,
        MANA_NAME VARCHAR(20) NOT NULL,
        MANA_POSITION VARCHAR(20) NOT NULL
);

CREATE TABLE CLOTHES (
        CLOTH_ID INT PRIMARY KEY,
        OUTFITS_TYPE VARCHAR(20) NOT NULL,
        CUST_ID VARCHAR(30) NOT NULL,
        COLOR VARCHAR(20) NOT NULL,
        SEASON VARCHAR(20) NOT NULL,
        IMG_URL VARCHAR(100) NOT NULL
        
        CONSTRAINT CLOTHES_FK FOREIGN KEY (CUST_ID) REFERENCES CUSTOMERS(CUST_ID)
);

CREATE TABLE OUTFITS (
        CUST_ID VARCHAR(30) NOT NULL,
        OUTER VARCHAR(20),
        TOP VARCHAR(20) NOT NULL,
        BOTTOM VARCHAR(20) NOT NULL,
        SEASON VARCHAR(20) NOT NULL,
        
        CONSTRAINT OUTFITS_FK FOREIGN KEY (CUST_ID) REFERENCES CUSTOMERS(CUST_ID)
);

